def _create_file_mention_edges(
    session: Any,
    collection_id: UUID,
    entity_name_to_id: dict[str, UUID],
    entity_source_files: dict[str, list[str]],
    file_node_by_uri: dict[str, UUID],
    edge_cls: Any,
    edge_kind_cls: Any,
    stats: dict,
) -> None:
    """Create FILE_MENTIONS_ENTITY edges for graph connectivity."""
    created: set[tuple[UUID, UUID]] = set()
    for entity_name, entity_id in entity_name_to_id.items():
        for uri in entity_source_files.get(entity_name, []):
            file_id = file_node_by_uri.get(uri)
            if not file_id or (file_id, entity_id) in created:
                continue
            session.add(
                edge_cls(
                    collection_id=collection_id,
                    source_node_id=file_id,
                    target_node_id=entity_id,
                    kind=edge_kind_cls.FILE_MENTIONS_ENTITY,
                    meta={"source_uri": uri},
                )
            )
            created.add((file_id, entity_id))
            stats["file_edges_created"] += 1


def _file_node_lookup_keys(natural_key: str) -> set[str]:
    """Return lookup keys for a FILE node natural key.

    The knowledge graph currently contains a mix of legacy ``file:...`` keys
    and plain document URIs. Semantic connectivity must work with both.
    """
    cleaned = str(natural_key or "").strip()
    if not cleaned:
        return set()

    keys = {cleaned}
    if cleaned.startswith("file:"):
        stripped = cleaned.split(":", 1)[1].strip()
        if stripped:
            keys.add(stripped)
    return keys


async def persist_semantic_entities(
    session: AsyncSession,
    collection_id: UUID,
    batch: ExtractionBatch,
    changed_doc_ids: list[str] | None = None,
) -> dict:
    """Persist extracted entities and relationships to the knowledge graph.

    Creates SEMANTIC_ENTITY nodes, SEMANTIC_RELATIONSHIP edges, and
    FILE_MENTIONS_ENTITY edges to connect semantic entities to their
    source files (for graph connectivity in retrieval).

    When ``changed_doc_ids`` is provided, only entities whose evidence
    references those documents are deleted before re-creation.  Entities
    from unchanged documents are preserved.

    Args:
        session: Database session
        collection_id: Collection UUID
        batch: Extraction batch to persist
        changed_doc_ids: Scope deletion to entities linked to these docs.
                         ``None`` deletes all entities (full rebuild).

    Returns:
        Stats dict
    """
    import uuid as uuid_module

    from contextmine_core.models import (
        Chunk,
        KnowledgeEdge,
        KnowledgeEdgeKind,
        KnowledgeEvidence,
        KnowledgeNode,
        KnowledgeNodeEvidence,
        KnowledgeNodeKind,
    )
    from sqlalchemy import delete, select

    stats = {
        "entities_created": 0,
        "relationships_created": 0,
        "file_edges_created": 0,
        "evidence_created": 0,
    }

    # Delete existing semantic entities — scoped to changed docs when possible
    if changed_doc_ids:
        # Only remove entities whose evidence references changed documents
        changed_uuids = [uuid_module.UUID(d) for d in changed_doc_ids]
        stale_q = (
            select(KnowledgeNode.id)
            .join(KnowledgeNodeEvidence, KnowledgeNodeEvidence.node_id == KnowledgeNode.id)
            .join(KnowledgeEvidence, KnowledgeNodeEvidence.evidence_id == KnowledgeEvidence.id)
            .join(Chunk, KnowledgeEvidence.chunk_id == Chunk.id)
            .where(
                KnowledgeNode.collection_id == collection_id,
                KnowledgeNode.kind == KnowledgeNodeKind.SEMANTIC_ENTITY,
                Chunk.document_id.in_(changed_uuids),
            )
            .distinct()
        )
        existing = await session.execute(stale_q)
        existing_ids = [row[0] for row in existing.fetchall()]
    else:
        # Full rebuild: delete all semantic entities for this collection
        existing = await session.execute(
            select(KnowledgeNode.id).where(
                KnowledgeNode.collection_id == collection_id,
                KnowledgeNode.kind == KnowledgeNodeKind.SEMANTIC_ENTITY,
            )
        )
        existing_ids = [row[0] for row in existing.fetchall()]

    if existing_ids:
        await session.execute(
            delete(KnowledgeEdge).where(
                KnowledgeEdge.source_node_id.in_(existing_ids)
                | KnowledgeEdge.target_node_id.in_(existing_ids)
            )
        )
        await session.execute(delete(KnowledgeNode).where(KnowledgeNode.id.in_(existing_ids)))

    # Build lookup of FILE nodes by URI for graph connectivity
    # This enables neighborhood traversal from search results to semantic entities
    file_result = await session.execute(
        select(KnowledgeNode.id, KnowledgeNode.natural_key).where(
            KnowledgeNode.collection_id == collection_id,
            KnowledgeNode.kind == KnowledgeNodeKind.FILE,
        )
    )
    file_node_by_uri: dict[str, UUID] = {}
    for node_id, natural_key in file_result.all():
        for lookup_key in _file_node_lookup_keys(natural_key):
            file_node_by_uri[lookup_key] = node_id

    # Create entity nodes
    entity_name_to_id: dict[str, UUID] = {}
    entity_source_files: dict[str, list[str]] = {}  # entity_name -> list of source URIs

    for entity in batch.entities:
        node = KnowledgeNode(
            collection_id=collection_id,
            kind=KnowledgeNodeKind.SEMANTIC_ENTITY,
            natural_key=entity.natural_key,
            name=entity.name,
            meta={
                "type": entity.entity_type,
                "description": entity.description,
                "aliases": entity.aliases,
                "source_symbols": entity.source_symbols,
                "source_files": entity.source_files,
            },
        )
        session.add(node)
        await session.flush()
        entity_name_to_id[entity.name.lower()] = node.id
        # Track source files for this entity
        entity_source_files[entity.name.lower()] = list(entity.source_files)
        for record in batch.entity_evidence.get(entity.name.lower(), []):
            evidence = KnowledgeEvidence(
                chunk_id=record.chunk_id,
                file_path=record.file_path,
                start_line=max(1, record.start_line),
                end_line=max(1, max(record.start_line, record.end_line)),
                snippet=record.snippet,
            )
            session.add(evidence)
            await session.flush()
            session.add(KnowledgeNodeEvidence(node_id=node.id, evidence_id=evidence.id))
            stats["evidence_created"] += 1
        stats["entities_created"] += 1

    # Create semantic relationship edges
    for rel in batch.relationships:
        source_id = entity_name_to_id.get(rel.source_entity.lower())
        target_id = entity_name_to_id.get(rel.target_entity.lower())

        if not source_id or not target_id:
            continue

        edge = KnowledgeEdge(
            collection_id=collection_id,
            source_node_id=source_id,
            target_node_id=target_id,
            kind=KnowledgeEdgeKind.SEMANTIC_RELATIONSHIP,
            meta={
                "relationship_type": rel.relationship_type,
                "description": rel.description,
                "strength": rel.strength,
            },
        )
        session.add(edge)
        stats["relationships_created"] += 1

    # Create FILE_MENTIONS_ENTITY edges to connect the code graph to semantic graph
    _create_file_mention_edges(
        session,
        collection_id,
        entity_name_to_id,
        entity_source_files,
        file_node_by_uri,
        KnowledgeEdge,
        KnowledgeEdgeKind,
        stats,
    )

    logger.info(
        "Persisted %d entities, %d relationships, %d file edges",
        stats["entities_created"],
        stats["relationships_created"],
        stats["file_edges_created"],
    )

    return stats
